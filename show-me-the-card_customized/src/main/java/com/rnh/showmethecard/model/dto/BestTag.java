package com.rnh.showmethecard.model.dto;

public class BestTag {

	
	private String columnName;
	private int valueNo;
	
	private String bestTagName;
	private String regDate;
	private String tagCount;
	
	
	public String getColumnName() {
		return columnName;
	}
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}
	public int getValueNo() {
		return valueNo;
	}
	public void setValueNo(int valueNo) {
		this.valueNo = valueNo;
	}
	public String getBestTagName() {
		return bestTagName;
	}
	public void setBestTagName(String bestTagName) {
		this.bestTagName = bestTagName;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getTagCount() {
		return tagCount;
	}
	public void setTagCount(String tagCount) {
		this.tagCount = tagCount;
	}
	
	
	
	
}
